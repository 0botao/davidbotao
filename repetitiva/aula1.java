package repetitiva;

public class aula1 {
    public static void main(String[] args) {
        int x = 1;

        int y = 1;

        while (x < 10) {
            System.out.println(x);
            x = x + 1; // ou x++
        }

        while (y < 10) {
            System.out.println(y);
            y++;
        }
    }
}
